function output = cloneCaseMCNP(caseDef,iteration)
global slash
[~,MCNPFileName,MCNPFileExt] = fileparts(caseDef.initialCases.MCNP);
MCNPFile = strcat(MCNPFileName,MCNPFileExt);
cd(sprintf('%s',caseDef.outputDir));
iterationDirName = strcat("MCNP",slash,num2str(iteration));
mkdir(sprintf('%s',iterationDirName));
cd(sprintf('%s',iterationDirName));
copyfile(caseDef.initialCases.MCNP);
output = char(strcat(caseDef.outputDir,slash,"MCNP",slash,num2str(iteration),slash,MCNPFile));
end
