function output = isNewCardMCNP(line,previousline)
output = 0;
characters = char(line);
for i=1:1:min(5,length(line))
   if characters(i)~= ' '
       output = 1;
   end
end

if ~isempty(previousline) 
    if (previousline{1,end}== '&')
           output = 0;
    end
end

if ~isempty(previousline) 
    if (previousline{1,end-1.*(length(previousline)>1)}== '&')
           output = 0;
    end
end

end
