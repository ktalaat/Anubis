function output = getExtMCNP(Temp)
if Temp <= 250 || Temp >= 2725
    disp("Fatal error: unsupported temperature.");
   return; 
end
extChar = "c";
extNum = floor((Temp-250)/25)+1;
if extNum >= 10
extNum = num2str(extNum);
end
if extNum <10
extNum = strcat("0",num2str(extNum));    
end
output = strcat(".",extNum,extChar);
end
