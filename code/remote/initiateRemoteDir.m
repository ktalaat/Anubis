function initiateRemoteDir(caseDef,iteration,activeApp)
if iteration == 1
    s = sftp(caseDef.setup.(char(activeApp)).hostname,caseDef.setup.(char(activeApp)).username,"Password",caseDef.setup.(char(activeApp)).password);
    PD = cd(s);
    try
    mkdir(s,sprintf('%s',caseDef.setup.(char(activeApp)).remoteDir));
    catch
    end
    cd(s,sprintf('%s',caseDef.setup.(char(activeApp)).remoteDir));
    mkdir(s,sprintf('%s',activeApp));
    cd(s,sprintf('%s',PD));
    close(s)
end
end
