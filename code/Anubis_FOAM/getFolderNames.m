function output = getFolderNames(directory)
namestoignore = [".","..","system","constant","uniform","polyMesh"];
files = dir(directory);
dirflag = [files.isdir];
files = {files.name};
folderNames = files(dirflag);
folderNames = folderNames.';
i=1;
newFolderNames = [];
while (i<=size(folderNames,1))
    if ~max(ismember(folderNames(i),namestoignore()))
    newFolderNames = [newFolderNames;folderNames(i)];
    end
i = i +1;
end
output = cellstr(newFolderNames);
end
