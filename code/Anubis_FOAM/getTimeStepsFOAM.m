function output = getTimeStepsFOAM(caseDef)
directory = caseDef.simCase.OpenFOAM;
folderNames = getFolderNames(directory);
flags = [];
for i=1:1:size(folderNames,1)
    try 
        folderName = cellfun(@str2num, folderNames(i,1));
        flags = [flags;1];
    catch
        flags = [flags;0];
    end
end
flags = logical(flags);
output.str = folderNames(flags);
output.int = cellfun(@str2num, output.str(:,1));
[~, maxStepIndex] = max(output.int);
output.last = output.str{maxStepIndex};
end
