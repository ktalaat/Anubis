function output = updateSurfacesObject(surfaces,surfExp,mapTempField,caseDef)
if caseDef.geometricalOperations.thermalExpansionCorrection == "yes"
surfaceEditList = fields(surfExp.surfaces);
for i=1:1:length(surfaceEditList)
statement = char(surfExp.surfaces.(char(surfaceEditList(i))));
value = resolveSurfaceExpVars(statement,surfExp,surfaces,mapTempField);
surfaceInfo = getParamsFromSurfaceName(char(surfaceEditList(i)));
surfaces.(char(surfaceInfo.surfaceID)).params{surfaceInfo.paramNum} = value;   
end
end
output = surfaces;
end
 