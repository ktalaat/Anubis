function output = getThermalExpCoeff(matDBID,temp,matDB)
if matDBID ~= '0'
 equation = matDB.(char(matDBID)).thermalExpCoeff;
 equation = strrep(equation,'temp',num2str(temp));
 output = str2num(equation);
end

if matDBID == '0'
    output = 0;
end

end
