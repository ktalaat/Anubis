function output = getParamsFromSurfaceName(surfaceVarName)
[~,b] = strtok(surfaceVarName,'_');
[surfaceID,paramNum] = strtok(b,'_');
surfaceID = strcat("s",surfaceID);
paramNum = strtok(paramNum,'_');
output.surfaceID = surfaceID;
output.paramNum = str2num(sprintf('%s',paramNum)); 
end
