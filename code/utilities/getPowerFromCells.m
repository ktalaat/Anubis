function output = getPowerFromCells(cells,CFDRegion,geomCoupling)
cellNames = fields(cells);
try
    for i=1:1:length(cellNames)
       if strcmpi(cells.(char(cellNames(i))).CFDRegion,CFDRegion)
           output = cells.(char(cellNames(i))).power;
       end        
    end
    
catch    
    try
            for i=1:1:length(cellNames)
               if strcmpi(char(cellNames(i)),strcat('c',geomCoupling.regions.(char(CFDRegion)).MCNPCellID))
                   output = cells.(char(cellNames(i))).power;
               end        
            end
    catch
    end
end

end
