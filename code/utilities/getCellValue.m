function output = getCellValue(handle,cellID,mapTempField)
switch handle
    case "material"
        handleID = 2;
    case "Temp"
        handleID = 3;
    case "matDBID"
        handleID = 4;
    case "refTemp"
        handleID = 5;
    case "alpha"
        handleID = 6;
    case "densityCorrection"
        handleID = 7;
    case "maxTemp"
        handleID = 9;
    otherwise
end
     
for i=1:1:size(mapTempField,1)
    if mapTempField{i,1} == cellID
       output = mapTempField{i,handleID}; 
    end
end
end
