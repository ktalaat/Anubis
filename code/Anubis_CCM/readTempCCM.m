function output = readTempCCM(caseDef,geomCoupling,iteration)
global slash
regionNames = fields(geomCoupling.regions);
for i=1:1:size(regionNames,1)
    fileAddress = char(strcat(caseDef.outputDir,slash,"StarCCM",slash,num2str(iteration),slash,"temperature.csv"));
    regionID = str2num(geomCoupling.regions.(char(regionNames(i))).CCMRegionID);
    result.mean.(char(regionNames(i)))= getTempFieldAverageCCM(fileAddress,regionID);
    result.max.(char(regionNames(i)))= getTempFieldMaxCCM(fileAddress,regionID);
end
output = result;
end
